    private void popularTabela() {
        try {
            ClienteDao cli = new ClienteDao();
            List<Cliente> listaCliente = cli.listarClientes();
            DefaultTableModel model = (DefaultTableModel) tblCliente.getModel();
            List<Object> lista = new ArrayList<Object>();

            for (int i = 0; i < listaCliente.size(); i++) {
                Cliente c = listaCliente.get(i);
                lista.add(new Object[]{c.getCodcli(), c.getNomcli(), c.getEndcli(), c.getBaicli(), c.getComcli(), c.getCepcli(), c.getCelcli()});

            }
            for (int p = 0; p < lista.size(); p++) {
                model.addRow((Object[]) lista.get(p));

            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Ocoreu Um erro ao obter os dados dos clientes do banco de dados");
        }

    }

